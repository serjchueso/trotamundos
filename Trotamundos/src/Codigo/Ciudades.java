package Codigo;

public class Ciudades {
	
	private String nombre;
	private String descripcion;
	private String transporte;
	private String lugaresYcomollegar;
	
	public Ciudades(String nombre, String descripcion, String transporte, String lugaresYcomollegar) {
		this.nombre = nombre;
		this.descripcion = descripcion;
		this.transporte = transporte;
		this.lugaresYcomollegar = lugaresYcomollegar;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getTransporte() {
		return transporte;
	}

	public void setTransporte(String transporte) {
		this.transporte = transporte;
	}

	public String getLugaresYcomollegar() {
		return lugaresYcomollegar;
	}

	public void setLugaresYcomollegar(String lugaresYcomollegar) {
		this.lugaresYcomollegar = lugaresYcomollegar;
	}

	@Override
	public String toString() {
		return "Ciudades [nombre=" + nombre + ", descripcion=" + descripcion + ", transporte=" + transporte
				+ ", lugaresYcomollegar=" + lugaresYcomollegar + "]";
	}
	

}
